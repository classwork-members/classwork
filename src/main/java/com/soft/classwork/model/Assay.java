package com.soft.classwork.model;

public class Assay {
    private int assayid;
    private String assayname;
    private String assaydesc;
    private String department;

    public int getAssayid() {
        return assayid;
    }

    public void setAssayid(int assayid) {
        this.assayid = assayid;
    }

    public String getAssayname() {
        return assayname;
    }

    public void setAssayname(String assayname) {
        this.assayname = assayname;
    }

    public String getAssaydesc() {
        return assaydesc;
    }

    public void setAssaydesc(String assaydesc) {
        this.assaydesc = assaydesc;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }
}
